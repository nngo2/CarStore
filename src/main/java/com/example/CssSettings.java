package com.example;

public class CssSettings {
    private String loginErrorMessage;
    private String loginErrorMessageClass;

    public String getLoginErrorMessage() {
        return loginErrorMessage;
    }

    public void setLoginErrorMessage(String loginErrorMessage) {
        this.loginErrorMessage = loginErrorMessage;
    }

    public String getLoginErrorMessageClass() {
        return loginErrorMessageClass;
    }

    public void setLoginErrorMessageClass(String loginErrorMessageClass) {
        this.loginErrorMessageClass = loginErrorMessageClass;
    }
}
